        genClass = '[object GeneratorFunction]';
    isCallable = function isCallable(value) {
        if (typeof value !== 'function') {
            return false;
        }
        if (hasToStringTag) {
            return tryFunctionObject(value);
        }
        var strClass = to_string.call(value);
        return strClass === fnClass || strClass === genClass;
    };
    var isRegex; /* inlined from https://npmjs.com/is-regex */
    var regexExec = RegExp.prototype.exec,
        tryRegexExec = function tryRegexExec(value) {
            try {
                regexExec.call(value);
                return true;
            } catch (e) {
                return false;
            }
        },
        regexClass = '[object RegExp]';
    isRegex = function isRegex(value) {
        if (typeof value !== 'object') {
            return false;
        }
        return hasToStringTag ? tryRegexExec(value) : to_string.call(value) === regexClass;
    };
    var isString; /* inlined from https://npmjs.com/is-string */
    var strValue = String.prototype.valueOf,
        tryStringObject = function tryStringObject(value) {
            try {
                strValue.call(value);
                return true;
            } catch (e) {
                return false;
            }
        },
        stringClass = '[object String]';
    isString = function isString(value) {
        if (typeof value === 'string') {
            return true;
        }
        if (typeof value !== 'object') {
            return false;
        }
        return hasToStringTag ? tryStringObject(value) : to_string.call(value) === stringClass;
    };

    var isArguments = function isArguments(value) {
        var str = to_string.call(value);
        var isArgs = str === '[object Arguments]';
        if (!isArgs) {
            isArgs = !isArray(value) &&
                value !== null &&
                typeof value === 'object' &&
                typeof value.length === 'number' &&
                value.length >= 0 &&
                isCallable(value.callee);
        }
        return isArgs;
    };


    /* inlined from http://npmjs.com/define-properties */
    var defineProperties = (function(has) {
        var supportsDescriptors = Object.defineProperty && (function() {
            try {
                Object.defineProperty({}, 'x', {});
                return true;
            } catch (e) { /* this is ES3 */
                return false;
            }
        }());

        // Define configurable, writable and non-enumerable props
        // if they don't exist.
        var defineProperty;
        if (supportsDescriptors) {
            defineProperty = function(object, name, method, forceAssign) {
                if (!forceAssign && (name in object)) {
                    return;
                }
                Object.defineProperty(object, name, {
                    configurable: true,
                    enumerable: false,
                    writable: true,
                    value: method
                });
            };
        } else {
            defineProperty = function(object, name, method, forceAssign) {
                if (!forceAssign && (name in object)) {
                    return;
                }
                object[name] = method;
            };
        }
        return function defineProperties(object, map, forceAssign) {
            for (var name in map) {
                if (has.call(map, name)) {
                    defineProperty(object, name, map[name], forceAssign);
                }
            }
        };
    }(ObjectPrototype.hasOwnProperty));


